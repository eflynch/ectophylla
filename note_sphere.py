class NoteSphere(object):
	def __init__(self, pitch, velocity, duration, start_pos, end_pos, time, x, y, tp, tick):
		self.pitch = pitch
		self.velocity = velocity
		self.duration = duration
		self.start_pos = start_pos
		self.end_pos = end_pos
		self.time = time
		self.x = x
		self.y = y
		self.tp = tp
		self.tick = tick

